package com.hash.bean;

public class ItemBean {

	private Integer id;
	private String iMaster;
	private Integer iCode;
	private String iName;
	private Integer iQty;
	

	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getiMaster() {
		return iMaster;
	}
	public void setiMaster(String iMaster) {
		this.iMaster = iMaster;
	}
	public Integer getiCode() {
		return iCode;
	}
	public void setiCode(Integer iCode) {
		this.iCode = iCode;
	}
	public String getiName() {
		return iName;
	}
	public void setiName(String iName) {
		this.iName = iName;
	}
	public Integer getiQty() {
		return iQty;
	}
	public void setiQty(Integer iQty) {
		this.iQty = iQty;
	}
	
	
}
